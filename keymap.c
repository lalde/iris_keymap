bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);

      } else {
        layer_off(_LOWER);

      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);

      } else {
        layer_off(_RAISE);

      }
      return false;
      break;

  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
  if (index == 0) {
      switch(biton32(layer_state)){

          case _QWERTY:
            if (clockwise) {
            tap_code(KC_VOLU);
            } else {
            tap_code(KC_VOLD);
            }
            break;

          case _RAISE:
              if (clockwise){
                  tap_code(KC_RGHT);
              } else{
                  tap_code(KC_LEFT);
              }
              break;
            }
          }
}


// Runs just one time when the keyboard initializes.
// RGB underglow color changes to correspond with selected layer.

void matrix_scan_user(void) {
  static bool has_run;
  if (!has_run) {
    has_run = true;
        rgblight_setrgb (RGB_MAGENTA);
  }
}
uint32_t layer_state_set_user(uint32_t state) {
    switch (biton32(state)) {
    case _QWERTY:
        rgblight_setrgb(RGB_MAGENTA);
        break;
    case _LOWER:
        rgblight_setrgb(RGB_TURQUOISE);
        break;
    case _RAISE:
        rgblight_setrgb (RGB_GOLD);
        break;

    }
  return state;

}
